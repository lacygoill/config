digits = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]

# `min()` and `max()` give you the minimum and maximum number out of a list of numbers.
print(min(digits))
print(max(digits))
#     0
#     9

# `sum()` gives you the sum of all the numbers from a list.
print(sum(digits))
#     45
