# Use `len()` to get the length of a list.
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))
#     4
#
# To determine the *index* of an item, Python starts counting from 0.
# To determine the *length* of a list, Python starts counting from 1.
