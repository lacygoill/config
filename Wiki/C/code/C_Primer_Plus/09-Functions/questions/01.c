// Purpose: What is the difference between an argument and a formal parameter?
//
// A  formal parameter  is a  variable  that is  defined in  the function  being
// called.  The  actual argument is  the value  appearing in the  function call;
// this value is assigned  to the formal argument.  You can  think of the actual
// argument as being the value to which the formal parameter is initialized when
// the function is called.
//
